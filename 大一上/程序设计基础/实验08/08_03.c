#include <stdio.h>

int main()
{
    // 12种本质不同的八皇后解（按字典序排列）
    // 数组的每个元素是一个长度为8的解，index表示行，值表示列（1~8）
    int solutions[12][8] = {
        {1, 5, 8, 6, 3, 7, 2, 4},
        {1, 6, 8, 3, 7, 4, 2, 5},
        {2, 4, 6, 8, 3, 1, 7, 5},
        {2, 5, 7, 1, 3, 8, 6, 4},
        {2, 5, 7, 4, 1, 8, 6, 3},
        {2, 6, 1, 7, 4, 8, 3, 5},
        {2, 6, 8, 3, 1, 4, 7, 5},
        {2, 7, 3, 6, 8, 5, 1, 4},
        {2, 7, 5, 8, 1, 4, 6, 3},
        {3, 5, 2, 8, 1, 7, 4, 6},
        {3, 5, 8, 4, 1, 7, 2, 6},
        {3, 6, 2, 5, 8, 1, 7, 4}};

    for (int i = 0; i < 11; i++)
    {
        printf("No%d:", i + 1);
        for (int j = 0; j < 8; j++)
        {
            printf("%d ", solutions[i][j]);
        }
        printf("\n");
    }
    printf("No12:");
    for (int j = 0; j < 8; j++)
    {
        printf("%d ", solutions[11][j]);
    }

    return 0;
}